%%
clear 
close all
clc


%% Simulation parameters
%% ------------------------------------------
dt = 0.05;
T = 5000;
t = 0:dt:T;
Steps = length(t);
%% ------------------------------------------

%% Model parameters
%% ------------------------------------------
F = 8;
n = 40;
%% ------------------------------------------

%% Observations
%% ------------------------------------------
yo = randn(n,1);
yC = model(yo,dt,Steps,F);
plot(t,yC(1,:))

save LongSim_n40.mat yC
